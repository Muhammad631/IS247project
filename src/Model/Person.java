package Model;

public abstract class Person {
    private String name;

    public Person(String name) {
        this.name = name;
    }

    // Getter and Setter
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Person [Name=" + name + "]";
    }
}